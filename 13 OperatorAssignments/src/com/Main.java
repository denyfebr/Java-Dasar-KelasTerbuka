public class Main {
    public static void main(String[] args) {
        // Operator assignment
        
        // Assignment operator jumlah
        int a = 1;
        a += 10; // a = a+10
        System.out.println("nilai a += 10 = "+a);
        System.out.println("================");
        // Assignment operator pengurangan
        int b = 100;
        b -= 25; // b = b - 25
        System.out.println("nilai b -= 25 = "+b);
        System.out.println("================");
        // Assignment operator perkalian
        int c = 100;
        c *= 20; // c = c * 25
        System.out.println("nilai c *= 25 = "+c);
        System.out.println("================");
        // Assignment operator pembagian
        int d = 100;
        d /= 20; // d = d / 25
        System.out.println("nilai d /= 25 = "+d);
        System.out.println("================");
        // Assignment operator modulus
        int e = 10;
        e %= 7; // e = e % 25
        System.out.println("nilai e %= 25 = "+e);
    }
}
